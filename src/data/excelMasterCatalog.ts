export function mainCategoryMenuToSlug(mainCategory: string): string {
  const key = normalizeExcelKey(mainCategory);
  for (const [needle, slug] of MAIN_SLUG_LOOKUP) {
    if (key === needle || key.includes(needle)) return slug;
  }
  return key.replace(/\s+/g, '-');
}

