/**
 * Master service list derived from the consultancy Excel workbook (PARTICULARS + MAIN CATEGORY MENU).
 * Order matters: the first row for a given logical service wins for URL main-category assignment.
 */
export type ExcelServiceRow = {
  name: string;
  mainCategory: string;
  /** Reserved for future sub-grouping; empty for this workbook. */
  subCategory: string;
};

export function normalizeExcelKey(value: string): string {
  return value
    .toLowerCase()
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/&/g, ' and ')
    .replace(/[^a-z0-9]+/g, ' ')
    .trim()
    .replace(/\s+/g, ' ');
}

export function excelParticularsSlug(name: string): string {
  return normalizeExcelKey(name).replace(/\s+/g, '-');
}

